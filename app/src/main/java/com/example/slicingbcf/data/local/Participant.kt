data class Participant(
  val namaLembaga : String,
  val batch : Int,
  val provinsi : String,
  val status : Status,
)

enum class Status {
  ACTIVE,
  INACTIVE,
  PENDING,
}

fun Status.toDisplayText() : String {
  return when (this) {
    Status.ACTIVE   -> "Aktif"
    Status.INACTIVE -> "Tidak Aktif"
    Status.PENDING  -> "Menunggu"
  }
}

fun Status.toColor() : Color {
  return when (this) {
    Status.ACTIVE   -> ColorPalette.Success600
    Status.INACTIVE -> ColorPalette.Danger500
    Status.PENDING  -> ColorPalette.Warning700
  }
}

